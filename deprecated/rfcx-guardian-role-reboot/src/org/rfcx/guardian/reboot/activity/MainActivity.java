package org.rfcx.guardian.reboot.activity;

import org.rfcx.guardian.reboot.R;
import org.rfcx.guardian.reboot.RfcxGuardian;
import org.rfcx.guardian.utility.ShellCommands;

import android.app.Activity;
import android.os.Bundle;
import android.view.Menu;
import android.view.MenuItem;

public class MainActivity extends Activity {

	@Override
	public boolean onCreateOptionsMenu(Menu menu) {
		getMenuInflater().inflate(R.menu.activity_home, menu);
		return true;
	}
	
	@Override
	public boolean onOptionsItemSelected(MenuItem item) {
		RfcxGuardian app = (RfcxGuardian) getApplication();
		switch (item.getItemId()) {

		case R.id.menu_reboot:
			ShellCommands.triggerReboot(getApplicationContext());
			break;

		case R.id.menu_root_command:
			ShellCommands.triggerNeedForRootAccess(getApplicationContext());
			break;
			
		}
		return true;
	}

	@Override
	public void onCreate(Bundle savedInstanceState) {
		super.onCreate(savedInstanceState);
		setContentView(R.layout.activity_home);
	}

	@Override
	public void onResume() {
		super.onResume();
	}

	@Override
	public void onPause() {
		super.onPause();
		((RfcxGuardian) getApplication()).appPause();
	}
	
}
